module PoolParty
  module Resources
    
    class FileResource < Resource
      
      default_options(
        :content => "# File generated by PoolParty"
      )
      
      def self.has_method_name
        "file"
      end
            
      def print_to_chef        
        <<-EOE
template "<%= name %>" do
  content "<%= content %>"
end
        EOE
      end
      
    end
    
  end
end